package com.digione.zgb2b.bean.product;

import java.io.Serializable;

/**
 * 品牌实体类
 * 
 * @author zhangqr
 * 
 */
public class BrandBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5277287083457096640L;

	/**
	 * 品牌ID
	 */
	private Integer brandId;

	/**
	 * 品牌名称
	 */
	private String brandName;

	/**
	 * 品牌产品列表URL
	 */
	private String brandSearchUrl;

	public Integer getBrandId() {
		return brandId;
	}

	public void setBrandId(Integer brandId) {
		this.brandId = brandId;
	}

	public String getBrandName() {
		return brandName;
	}

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public String getBrandSearchUrl() {
		return brandSearchUrl;
	}

	public void setBrandSearchUrl(String brandSearchUrl) {
		this.brandSearchUrl = brandSearchUrl;
	}

}
