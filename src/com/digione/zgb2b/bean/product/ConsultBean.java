package com.digione.zgb2b.bean.product;

import java.io.Serializable;
import java.util.List;

/**
 * 产品咨询
 * 
 * @author zhangqr
 * 
 */
public class ConsultBean implements Serializable {

	private static final long serialVersionUID = -1431123444849520716L;
	/**
	 * 总数据条数
	 */
	private Integer totalCount;
	/**
	 * 当前页码
	 */
	private Integer currentPage;
	/**
	 * 每页条数
	 */
	private Integer pageSize;
	/**
	 * 返回页数
	 */
	private Integer pageCount;
	/**
	 * 咨询内容列表
	 */
	private List<ConsultItemBean> list;

	public Integer getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(Integer totalCount) {
		this.totalCount = totalCount;
	}

	public Integer getCurrentPage() {
		return currentPage;
	}

	public void setCurrentPage(Integer currentPage) {
		this.currentPage = currentPage;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

	public Integer getPageCount() {
		return pageCount;
	}

	public void setPageCount(Integer pageCount) {
		this.pageCount = pageCount;
	}

	public List<ConsultItemBean> getList() {
		return list;
	}

	public void setList(List<ConsultItemBean> list) {
		this.list = list;
	}

}
