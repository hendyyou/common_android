package com.digione.zgb2b.bean.product;

import java.io.Serializable;

/**
 * 操作系统实体类
 * 
 * @author zhangqr
 * 
 */
public class OSBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2587261335192478049L;

	/**
	 * 操作系统ID
	 */
	private Integer operatingSystemId;

	/**
	 * 操作系统名称
	 */
	private String operatingSystemName;

	/**
	 * 操作系统产品列表URL
	 */
	private String operatingSystemSearchUrl;

	public Integer getOperatingSystemId() {
		return operatingSystemId;
	}

	public void setOperatingSystemId(Integer operatingSystemId) {
		this.operatingSystemId = operatingSystemId;
	}

	public String getOperatingSystemName() {
		return operatingSystemName;
	}

	public void setOperatingSystemName(String operatingSystemName) {
		this.operatingSystemName = operatingSystemName;
	}

	public String getOperatingSystemSearchUrl() {
		return operatingSystemSearchUrl;
	}

	public void setOperatingSystemSearchUrl(String operatingSystemSearchUrl) {
		this.operatingSystemSearchUrl = operatingSystemSearchUrl;
	}

}
