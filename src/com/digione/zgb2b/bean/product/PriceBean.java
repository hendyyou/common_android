package com.digione.zgb2b.bean.product;

import java.io.Serializable;

/**
 * 价格区间实体类
 * 
 * @author zhanqr
 * 
 */
public class PriceBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6595959570726493363L;

	/**
	 * 区间ID
	 */
	private String advicePriceRangeId;

	/**
	 * 区间名称
	 */
	private String advicePriceRangeName;

	/**
	 * 区间最大的值
	 */
	private String maxAdvicePrice;

	/**
	 * 区间最小的值
	 */
	private String minAdvicePrice;

	/**
	 * 区间对应的产品列表URL
	 */
	private String advicePriceRangeSearchUrl;

	public String getAdvicePriceRangeId() {
		return advicePriceRangeId;
	}

	public void setAdvicePriceRangeId(String advicePriceRangeId) {
		this.advicePriceRangeId = advicePriceRangeId;
	}

	public String getAdvicePriceRangeName() {
		return advicePriceRangeName;
	}

	public void setAdvicePriceRangeName(String advicePriceRangeName) {
		this.advicePriceRangeName = advicePriceRangeName;
	}

	public String getMaxAdvicePrice() {
		return maxAdvicePrice;
	}

	public void setMaxAdvicePrice(String maxAdvicePrice) {
		this.maxAdvicePrice = maxAdvicePrice;
	}

	public String getMinAdvicePrice() {
		return minAdvicePrice;
	}

	public void setMinAdvicePrice(String minAdvicePrice) {
		this.minAdvicePrice = minAdvicePrice;
	}

	public String getAdvicePriceRangeSearchUrl() {
		return advicePriceRangeSearchUrl;
	}

	public void setAdvicePriceRangeSearchUrl(String advicePriceRangeSearchUrl) {
		this.advicePriceRangeSearchUrl = advicePriceRangeSearchUrl;
	}

}
